s = s.replace(/deepseek:\s*'[^']*'/, "deepseek: 'D'");
s = s.replace(/llama:\s*'[^']*'/, "llama: 'L'");
s = s.replace(/mini:\s*'[^']*'/, "mini: 'Mi'");
s = s.replace(/gpt4o:\s*'[^']*'/, "gpt4o: 'G'");
s = s.replace(/[^\x00-\x7F]+\s*Konsiliumni Boshlash/g, 'Konsiliumni Boshlash');
s = s.replace(/[^\x00-\x7F]+\s*Kuzatuv Rejasi/g, 'Kuzatuv Rejasi');
fs.writeFileSync(p, s);
